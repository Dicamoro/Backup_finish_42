#include <stdio.h>

int	ft_str_is_lowercase(char *src)
{
	int	i;

	i = 0;
	if (src[i] == '\0')
	{
		return (1);
	}
	while (src[i] != '\0')
	{
		if ((src[i] < 'a' || src[i] > 'z'))
		{
			return (0);
		}
		src ++;
	}
	return (1);
}
